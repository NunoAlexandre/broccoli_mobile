import Foundation
import UIKit

extension UIColor {
    
    open class func broccoliYellow() -> UIColor {
        return UIColor(red: 255/255, green: 252/255, blue: 214/255, alpha: 1.0)
    }
    
    open class func broccoliDarkYellow() -> UIColor {
        return UIColor(red: 225/255, green: 220/255, blue: 181/255, alpha: 1.0)
    }
    
    open class func broccoliGreen() -> UIColor {
        return UIColor(red: 136/255, green: 178/255, blue: 70/255, alpha: 1.0)
    }
    
    open class func broccoliGreenLighter() -> UIColor {
        return UIColor(red: 198/255, green: 211/255, blue: 95/255, alpha: 1.0)
    }
    
    open class func broccoliGreenDarker() -> UIColor {
        return UIColor(red: 177/255, green: 193/255, blue: 83/255, alpha: 1.0)
    }
}
